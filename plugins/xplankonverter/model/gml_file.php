class gml_file {
    
  function __construct($filename) {
    global $debug;
    $this->debug = $debug;
    $this->filename = $filename;
  }

  function delete(){
    if (is_file($this->file_name))
      unlink($this->file_name);
  }

	function exists() {
		return file_exists($this->filename);
	}
}

